    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int id = Integer.parseInt(jTextField1.getText().trim());
        String marca = jTextField2.getText().trim();
        String modelo = jTextField3.getText().trim();
        double precio = Double.parseDouble(jTextField4.getText().trim());
        String patente = jTextField5.getText().trim();
        int canPuertas = Integer.parseInt(jTextField6.getText().trim());
        
        try {
            boolean actualizado = vehiculoController.actualizarVehiculo(id,marca, modelo, patente, precio, canPuertas);
            if(actualizado){
                JOptionPane.showMessageDialog(null, "Auto actualizado.");
            }else{
                JOptionPane.showMessageDialog(null, "Auto no actualizado.");
            }
        } catch (SQLException ex) {
            System.getLogger(ActualizarForm.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


