    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int codigo = Integer.parseInt(jTextField1.getText().trim());
        if(codigo < 0){
            JOptionPane.showMessageDialog(null, "Codigo no valido!");
        }
        
        try {
            Vehiculo vehiculo = vehiculoController.buscarVehiculoPorCodigo(codigo);
            jTextField2.setText(vehiculo.getMarca());
            jTextField3.setText(vehiculo.getModelo());
            jTextField4.setText(String.valueOf(vehiculo.getPrecio()));
            jTextField5.setText(vehiculo.getPatente());
            jTextField6.setText(String.valueOf( ((Auto) vehiculo).getCanPuertas()));
        } catch (SQLException ex) {
            System.getLogger(ActualizarForm.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

